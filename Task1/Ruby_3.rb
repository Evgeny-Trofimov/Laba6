# Инициализация матрицы 3x3 с тестовыми значениями
matrix = [
  [5, 2, 3],
  [1, 4, 6],
  [9, 8, 7]
]

# Вывод заголовка матрицы
puts "Матрица:"
matrix.each do |row|
  puts row.join(' ')  # Вывод строки с элементами, разделенными пробелами
end

# Поиск элементов, минимальных в строке и максимальных в столбце
result = []

matrix.each_with_index do |row, i|
  # Находим минимальный элемент в строке и его индекс
  min_val = row.min
  col_index = row.index(min_val)

  # Проверяем, является ли этот элемент максимальным в столбце
  is_max = matrix.all? { |r| r[col_index] <= min_val }

  # Добавляем в результат, если соответствует условиям
  result << min_val if is_max
end

# Вывод результатов поиска
if result.empty?
  puts "Таких элементов нет."
else
  puts "Найденные элементы: #{result.join(' ')}"
end
