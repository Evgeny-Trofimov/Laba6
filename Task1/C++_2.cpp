#include <iostream>       
#include <vector>         

using namespace std;      

int main() {
    int N;  
    cout << "Введите размер матрицы (N): ";  
    cin >> N;  

    // Создаем квадратную матрицу N x N, заполненную нулями
    vector<vector<int>> matrix(N, vector<int>(N));

    // Заполняем матрицу по заданному шаблону
    for (int i = 0; i < N; ++i) {         // Цикл по строкам
        for (int j = 0; j < N; ++j) {      // Цикл по столбцам
            if (i == j) {                  // Если элемент на главной диагонали
                matrix[i][j] = 0;          // Записываем 0
            } else if (i < j) {            // Если элемент выше главной диагонали
                matrix[i][j] = 100 + (N - 1 - j);  // Формула для элементов сверху
            } else {                        // Если элемент ниже главной диагонали
                matrix[i][j] = 100 + (j);   // Формула для элементов снизу
            }
        }
    }

    // Выводим матрицу с заменой 0 на "000"
    for (int i = 0; i < N; ++i) {          // Цикл по строкам
        for (int j = 0; j < N; ++j) {      // Цикл по столбцам
            if (matrix[i][j] == 0) {       // Если элемент равен 0
                cout << "000" << "\t";     // Выводим "000" с табуляцией
            } else {                       // Иначе
                cout << matrix[i][j] << "\t";  // Выводим значение элемента с табуляцией
            }
        }
        cout << endl;  // Переход на новую строку после вывода всей строки матрицы
    }

    return 0;  
}
